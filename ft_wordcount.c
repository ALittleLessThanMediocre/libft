#include "libft.h"

size_t		ft_wordcount(const char *s, char c)
{
	unsigned int	i;
	size_t			words;

	i = 0;
	words = 0;
	while (s[i])
	{
		if (s[i] != c)
			words++;
		while (s[i] != c && s[i + 1])
			i++;
		i++;
	}
	return (words);
}
