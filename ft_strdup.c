#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	len;

	len = ft_strlen(s1) + 1;
	if (!(s2 = malloc(len)))
		return (NULL);
	return (ft_memcpy(s2, s1, len));
}
